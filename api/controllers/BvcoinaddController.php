namespace api\controllers;


use yii\rest\ActiveController;
use yii\data\ActiveDataProvider;
use api\models\TabVcoinbindAdd;

class BvcoinaddController extends ActiveController
{
    public $modelClass="api\models\TabVcoinbindAdd";
    public $serializer = [
        'class' => 'yii\rest\Serializer',
        'collectionEnvelope' => 'items',
    ];
    public function actions()
    {
        $actions= parent::actions(); // TODO: Change the autogenerated stub
        unset($actions['delete'],$actions['create'],$actions['update'],$actions['index'],$actions['view']);
        return $actions;
    }

    public function actionIndex($sku,$serverId,$db,$playerName,$src,$addvc,$from,$to)
    {
        TabVcoinbindAdd::setDBPrefix($sku,$serverId,$db);
        $query=TabVcoinbindAdd::find();
        //必要字段
        $query->where(['playername'=>$playerName])
            ->andFilterWhere([//非必要字段
                'src'=>$src,
                'addvc'=>$addvc,
            ])
            ->andFilterWhere(['>=','logtime',$from])
            ->andFilterWhere(['<=','logtime',$to]);
        $data=new ActiveDataProvider([
            'query'=>$query,
        ]);
        return $data;
    }
}